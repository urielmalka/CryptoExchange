#Create DataBase

CREATE SCHEMA coins;

USE coins;
SET SQL_SAFE_UPDATES = 0;

CREATE TABLE API
(
	AID INT
    ,
    api varchar(100)
    ,
    PRIMARY KEY(AID)    
);


CREATE TABLE Coins
(
	CID INT
    ,
    CNAME varchar(20)
    ,
    PRIMARY KEY(CID)    
);

CREATE TABLE balance
(
	CID INT
    ,
    Amount DOUBLE
    ,
	FOREIGN KEY (CID) REFERENCES coins(CID)
);

CREATE TABLE coins_value
(
	CID INT
    ,
    dollar_value DOUBLE
    ,
	FOREIGN KEY (CID) REFERENCES coins(CID)
);

CREATE TABLE Favorite
(
	CID INT
    ,
	FOREIGN KEY (CID) REFERENCES Coins(CID)
);

